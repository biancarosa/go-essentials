package main

import "fmt"

func main() {

	// Olá, assim eu faço um Hello World em Go!
	fmt.Println("Hello, Go Essentials!") // String

	fmt.Println(2) // Inteiros
	fmt.Printf("Hello, I'm %v and I'm an integer\n", 2)
	fmt.Printf("Hello, I'm %d and I'm an integer\n", 2)

	fmt.Println(2.2 + 5) // Expressões

	// Booleanos
	fmt.Println(true)
	fmt.Println(false)
}
